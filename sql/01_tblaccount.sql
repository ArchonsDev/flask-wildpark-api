CREATE TABLE tblaccount (
    id INT PRIMARY KEY AUTO_INCREMENT,
    birthdate DATE,
    contact_no VARCHAR(255),
    country VARCHAR(255),
    email VARCHAR(255),
    firstname VARCHAR(255),
    gender VARCHAR(255),
    lastname VARCHAR(255),
    municipality VARCHAR(255),
    password VARCHAR(255),
    province VARCHAR(255),
    role VARCHAR(255),
    street VARCHAR(255),
    zip_code INT
);